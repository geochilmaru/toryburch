DROP TABLE IF EXISTS TORY_PROD;
CREATE TABLE TORY_PROD (
	ROW_ID INTEGER PRIMARY KEY AUTOINCREMENT,
	CATEGORY VARCHAR(100),
	CATEGORY_URL VARCHAR(100),
	NAME VARCHAR(100),
	FULL_NAME VARCHAR(100),
	DESC VARCHAR(100),
	STANDARD_PRICE VARCHAR(100),
	SALES_PRICE VARCHAR(100),
	URL VARCHAR(100),
	IMG_URL VARCHAR(100),
	ALT_IMG_URL VARCHAR(100),
	ALT_IMG_DESC VARCHAR(100),
	DETAILS VARCHAR(500),
	STATUS VARCHAR(100),	-- EXPIRED, ACTIVE
	CREATED DATE,
	LAST_UPD DATE
);

DROP TABLE IF EXISTS TORY_PRICE;
CREATE TABLE TORY_PRICE (
	ROW_ID INTEGER PRIMARY KEY AUTOINCREMENT,
	PAR_ROW_ID INTEGER,
	STANDARD_PRICE VARCHAR(100),
	SALES_PRICE VARCHAR(100),
	CREATED DATE,
	LAST_UPD DATE
);

DROP TABLE IF EXISTS TORY_COLOR;
CREATE TABLE TORY_COLOR (
	ROW_ID INTEGER PRIMARY KEY AUTOINCREMENT,
	PAR_ROW_ID INTEGER,
	NAME VARCHAR(100),
	CODE VARCHAR(100),
	URL VARCHAR(100),
	IMG_URL VARCHAR(100),
	CREATED DATE,
	LAST_UPD DATE
);


